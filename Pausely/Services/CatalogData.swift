import Foundation

// MARK: - Entry Helper

func makeCatalogEntry(
    bundleId: String,
    name: String,
    category: SubscriptionCategory,
    description: String,
    cancellationURL: String? = nil,
    tiers: [TierPricing]
) -> CatalogEntry {
    CatalogEntry(
        id: UUID(),
        bundleId: bundleId,
        name: name,
        category: category,
        description: description,
        iconName: iconForCatalogCategory(category),
        appStoreProductId: nil,
        websiteURL: "https://\(name.lowercased().replacingOccurrences(of: " ", with: "")).com",
        cancellationURL: cancellationURL,
        trialDays: 0,
        canPause: true,
        supportedTiers: tiers,
        lastUpdated: Date()
    )
}

func iconForCatalogCategory(_ category: SubscriptionCategory) -> String {
    switch category {
    case .entertainment: return "tv"
    case .music: return "music.note"
    case .productivity: return "briefcase"
    case .healthFitness: return "heart.circle"
    case .cloudStorage: return "cloud"
    case .education: return "book"
    case .utilities: return "wrench"
    case .finance: return "dollarsign.circle"
    case .food: return "fork.knife"
    case .shopping: return "cart"
    case .sports: return "sportscourt"
    case .social: return "person.2"
    case .news: return "newspaper"
    case .phone: return "iphone"
    case .insurance: return "shield.checkered"
    case .gym: return "dumbbell"
    case .automotive: return "car"
    case .home: return "house"
    case .pet: return "pawprint"
    case .personalCare: return "sparkles"
    case .aiTools: return "sparkles.square.fill.on.square"
    case .gaming: return "gamecontroller"
    case .developerTools: return "chevron.left.forwardslash.chevron.right"
    case .creator: return "pencil.circle"
    case .travel: return "airplane"
    case .dating: return "heart"
    case .kids: return "figure.and.child.holdinghands"
    case .security: return "lock.shield"
    case .other: return "square.grid.2x2"
    }
}

// MARK: - Expanded Catalog Builders

func buildExpandedCatalog() -> [CatalogEntry] {
    var entries: [CatalogEntry] = []
    entries.append(contentsOf: buildAIToolsCatalog())
    entries.append(contentsOf: buildGamingCatalog())
    entries.append(contentsOf: buildDeveloperToolsCatalog())
    entries.append(contentsOf: buildCreatorCatalog())
    entries.append(contentsOf: buildTravelCatalog())
    entries.append(contentsOf: buildDatingCatalog())
    entries.append(contentsOf: buildKidsCatalog())
    entries.append(contentsOf: buildSecurityCatalog())
    entries.append(contentsOf: buildNicheCatalog())
    entries.append(contentsOf: buildExpandedExistingCatalog())
    entries.append(contentsOf: buildPauselyProCatalog())
    return entries
}

